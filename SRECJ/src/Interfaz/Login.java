    private void txtEntrarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_txtEntrarMouseClicked
        // TODO add your handling code here:
        MenuUsuario mn = new MenuUsuario();
        MenuAbogado ma = new MenuAbogado();
        MenuPresidente mp = new MenuPresidente();
        Usuario aux = lt.login(campoCorreo.getText(), campoClave.getText());
        String tipoUsuario = aux.getTipo();
        if (!verificarVacio(campoCorreo.getText()) && !verificarVacio(campoClave.getText()) ||( !lt.buscarCorreo(campoCorreo.getText())  && !lt.buscarClave(campoClave.getText()))) {
            switch (tipoUsuario) {
                case "Abogado":
                    ma.usu = aux;
                    ma.lt = lt;
                    ma.denuncia = denuncia;
                    ma.denuncias = denuncias;
                    ma.colacpusu = colacpusu;
                    ma.colatotal = colatotal;
                    ma.setVisible(true);
                    this.dispose();
                    break;
                case "Comun":
                    mn.usu = aux;
                    mn.lt = lt;
                    mn.denuncia = denuncia;
                    mn.denuncias = denuncias;
                    mn.colacpusu = colacpusu;
                    mn.colatotal = colatotal;
                    mn.setVisible(true);
                    this.dispose();
                    break;
                case "Presidente":
                    mp.usu = aux;
                    mp.lt = lt;
                    mp.denuncia = denuncia;
                    mp.denuncias = denuncias;
                    mp.colacpusu = colacpusu;
                    
                    mp.colatotal = colatotal;
                    mp.setVisible(true);
                    this.dispose();
                    break;
                default:
                    JOptionPane.showMessageDialog(null, "DATOS INCORRECTOS");
                    break;
            }
            
        } else
            JOptionPane.showMessageDialog(null, "DATOS INCORRECTOS");
    }//GEN-LAST:event_txtEntrarMouseClicked

